    private void consultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultarActionPerformed
        // TODO add your handling code here:
        String Monto, Destinatario, Id, query;
        String SUrl, SUser, SPass;
        SUrl = "jdbc:MySQL://localhost:3306/remesadora";
        SUser = "root";
        SPass = "Yosoyesemismo.1025";
        
        Monto = monto.getText();
        Destinatario = destinatario.getText();
        Id = ID.getText();

        try (Connection conn = DriverManager.getConnection(SUrl, SUser, SPass)) {
            query = "SELECT * FROM envios WHERE monto = ? AND destinatario = ? AND id = ?";
            try (PreparedStatement pstmt = conn.prepareStatement(query)) {
                pstmt.setString(1, Monto);
                pstmt.setString(2, Destinatario);
                pstmt.setString(3, Id);
                try (ResultSet rs = pstmt.executeQuery()) {
                    if (rs.next()) {
                        JOptionPane.showMessageDialog(null, "¡Remesa encontrada!");
                        monto.setText("");
                        destinatario.setText("");
                        fecha.setText("");
                        ID.setText("");
                    } else {
                        JOptionPane.showMessageDialog(null, "No se encontró ninguna remesa con el monto, destinatario y ID especificados.");
                    }
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error al consultar la remesa: " + e.getMessage());
        }        
    }//GEN-LAST:event_consultarActionPerformed

