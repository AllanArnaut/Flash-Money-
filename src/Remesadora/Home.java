    private void retirarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_retirarActionPerformed
        // TODO add your handling code here:
        String Monto, Destinatario, Id, Moneda, Cambio, Fecha, nameUser, query;
        int RegUser;
        double montoAct = 0;
        String SUrl, SUser, SPass;
        SUrl = "jdbc:MySQL://localhost:3306/remesadora";
        SUser = "root";
        SPass = "Yosoyesemismo.1025";
        
        Monto = monto.getText();
        Destinatario = destinatario.getText();
        Id = ID.getText();
        Moneda = moneda;
        Fecha = fecha.getText();
        RegUser = savUser;
        nameUser = user.getText();              
        
        try (Connection conn = DriverManager.getConnection(SUrl, SUser, SPass)) {
            Statement st = conn.createStatement();
            query = "SELECT * FROM envios WHERE monto = ? AND destinatario = ? AND id = ?";
            try (PreparedStatement pstmt = conn.prepareStatement(query)) {
                pstmt.setString(1, Monto);
                pstmt.setString(2, Destinatario);
                pstmt.setString(3, Id);
                try (ResultSet rs = pstmt.executeQuery()) {
                    if (rs.next()){
                        
                        ResultSet resultSet = st.executeQuery("SELECT moneda FROM envios");
                        
                        if (resultSet.next()) {
                            Cambio = resultSet.getString("moneda");
                            System.out.println("El ultimo valor de moneda es: " + Cambio);
                            System.out.println("El ultimo valor de moneda es: " + Moneda);
                        
                            if ("Dolares".equals(Cambio) && "Pesos".equals(Moneda)){
                                montoAct = 57.60 * Double.parseDouble(Monto);
                                System.out.println(Moneda +" " + montoAct);
                            } else if ("Euros".equals(Cambio) && "Pesos".equals(Moneda)){
                                montoAct = 60 * Double.parseDouble(Monto);
                                System.out.println(Moneda +" " + montoAct);
                            } else if ("Euros".equals(Cambio) && "Dolares".equals(Moneda)){
                                JOptionPane.showMessageDialog(null, "Solo se puede retirar en pesos o moneda origen");
                                System.out.println(Moneda +" " + montoAct);
                            } else {
                                montoAct = Double.parseDouble(Monto);
                            } 
                            
                            if (!"Euros".equals(Cambio) && "Dolares".equals(Moneda) || !"Dolares".equals(Cambio) && "Euros".equals(Moneda) ||"Dolares".equals(Cambio) && "Pesos".equals(Moneda) || "Euros".equals(Cambio) && "Pesos".equals(Moneda) ){
                                JOptionPane.showMessageDialog(null, "¡Remesa retirada!\n\n Destinatario: "+ Destinatario + "\nMonto enviado: " + Monto + "\nMonto recibido: " + montoAct );
                                query = "INSERT INTO retiros( monto, receptor, fecha_retiro, moneda_retiro, usuario_id, usuario)" +
                                " VALUES( '" + montoAct + "', '" + Destinatario + "', '" + Fecha + "', '" + Moneda + "', '" + RegUser + "', '" + nameUser + "')";
                                st.execute(query);
                            }
                        }
               
                        monto.setText("");
                        destinatario.setText("");
                        fecha.setText("");
                        ID.setText("");
                    } else {
                        JOptionPane.showMessageDialog(null, "No se encontró ninguna remesa con el monto, destinatario y ID especificados.");
                    }
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error al consultar la remesa: " + e.getMessage());
        }  
    }//GEN-LAST:event_retirarActionPerformed

